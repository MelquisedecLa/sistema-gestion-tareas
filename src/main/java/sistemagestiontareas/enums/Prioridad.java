package sistemagestiontareas.enums;

/**
 * Representa los niveles de prioridad que puede tener una tarea
 */
public enum Prioridad {

    /** Prioridad alta: la tarea es urgente */
    ALTA,

    /** Prioridad media: la tarea es importante pero no urgente */
    MEDIA,

    /** Prioridad baja: la tarea puede realizarse después */
    BAJA
}